#49. How to find the mean using “sum() and len()” on Python?
list=[35,48,52,57,68,84]
 
total = sum(list)
piece = len(list)
print(total/piece)                 #o/p is 57.433
print(total//piece)                 #o/p is 57
                                    #sum() is adding of list values and len() is number of values in list